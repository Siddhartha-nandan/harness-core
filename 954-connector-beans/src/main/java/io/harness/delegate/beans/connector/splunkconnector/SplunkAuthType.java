package io.harness.delegate.beans.connector.splunkconnector;

import io.harness.annotations.dev.HarnessTeam;
import io.harness.annotations.dev.OwnedBy;

import com.fasterxml.jackson.annotation.JsonProperty;
import com.fasterxml.jackson.annotation.JsonValue;

@OwnedBy(HarnessTeam.CV)
public enum SplunkAuthType {
  @JsonProperty(SplunkConstant.USERNAME_PASSWORD) USER_PASSWORD(SplunkConstant.USERNAME_PASSWORD),
  @JsonProperty(SplunkConstant.ANONYMOUS) ANONYMOUS(SplunkConstant.ANONYMOUS),
  @JsonProperty(SplunkConstant.BEARER_TOKEN) BEARER_TOKEN(SplunkConstant.BEARER_TOKEN);

  private final String displayName;

  SplunkAuthType(String displayName) {
    this.displayName = displayName;
  }

  public String getDisplayName() {
    return displayName;
  }

  @Override
  public String toString() {
    return displayName;
  }

  @JsonValue
  final String displayName() {
    return this.displayName;
  }
}
